public class Achievement {
    private int id;
    private int userId;
    private int numberDate;
    private int numberFolder;
    private int numberStudy;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public int getNumberDate() {
        return numberDate;
    }

    public void setNumberDate(int numberDate) {
        this.numberDate = numberDate;
    }

    public int getNumberFolder() {
        return numberFolder;
    }

    public void setNumberFolder(int numberFolder) {
        this.numberFolder = numberFolder;
    }

    public int getNumberStudy() {
        return numberStudy;
    }

    public void setNumberStudy(int numberStudy) {
        this.numberStudy = numberStudy;
    }
}