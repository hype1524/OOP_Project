public class Quiz {
    private String question;
    private String a;
    private String b;
    private String c;
    private String d;
    private int ansPosition;

    public String getQuestion() {
        return question;
    }

    public String getA() {
        return a;
    }

    public String getB() {
        return b;
    }

    public String getC() {
        return c;
    }

    public String getD() {
        return d;
    }

    public int getAnsPosition() {
        return ansPosition;
    }
}
